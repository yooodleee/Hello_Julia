# 논리 표현식

"""
논리 표현식(Boolean expression)은 평가 결과가 참(true)이거나 거짓(false)인 표현식이다.
다음 예제에서는 == 연산자를 사용하는데, 이는 두 피연산자를 비교해서 같으면, true, 거짓이면 false를 반환하는 연산자다.
"""

5 == 5
5 == 6

"""
여기서 true와 false는 문자열이 아니고, Bool 자료형에 속하는 특별한 값이다.
"""

typeof(true)
typeof(false)

"""
== 연산자는 관계 연산자(relational operator) 중 하나이다.
관계 연산자는 피연산자들을 비교하는 연산자다.
"""